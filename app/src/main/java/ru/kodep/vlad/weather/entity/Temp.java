public class Temp {

    private float day;
    private float min;
    private float max;
    private float night;
    private float eve;
    private float morn;


    public float getDay() {
        return day;
    }

    public float getMin() {
        return min;
    }

    public float getMax() {
        return max;
    }

    public float getNight() {
        return night;
    }

    public float getEve() {
        return eve;
    }

    public float getMorn() {
        return morn;
    }
}
